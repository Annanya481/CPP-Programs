// to accept values for c1, c2, c3 and print their values
#include <iostream>
using namespace std;
int main()
{
    int c1, c2, c3;
    cout<<"enter three values: ";
    cin>>c1>>c2>>c3;
    cout<<c1<<c2<<c3<<"\n";
    cout<<c1<<"\n"<<c2<<"\n"<<c3<<"\n";
    cout<<c1<<"\t"<<c2<<"\t"<<c3;
    return 0;
}
